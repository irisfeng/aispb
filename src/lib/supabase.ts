import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";

// Re-export browser-safe utilities so existing server imports still work
export { isSupabaseConfigured, createSupabaseBrowserClient } from "@/lib/supabase-browser";

// Server client (for API routes — can read AND write cookies)
export async function createSupabaseServerClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll: () => cookieStore.getAll(),
        setAll: (cookiesToSet) => {
          cookiesToSet.forEach(({ name, value, options }) => {
            cookieStore.set(name, value, options);
          });
        },
      },
    },
  );
}

